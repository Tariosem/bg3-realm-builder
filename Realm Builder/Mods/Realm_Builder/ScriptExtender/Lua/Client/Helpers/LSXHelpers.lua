end

--- generate a meta.lsx file for a mod
---@param uuid string
---@param modName string
---@param author string
---@param version vec4
---@param description string
---@return LSXNode, LSXNode, LSXNode -- root, conflictNode, dependenciesNode
function LSXHelpers.BuildModMeta(uuid, modName, internalName, author, version, description)
    uuid = uuid or Uuid_v4()
    modName = modName or "Unnamed Mod"
    author = author or "Unknown Author"
    version = version or { 1, 0, 0, 0 }
    description = description or ""

    local version64 = ComputeVersion64(version[1], version[2], version[3], version[4]) --[[@as number|string]]

    local root = LSXHelpers.new()
    root:GetChild(1):SetAttribute("lslib_meta", nil)

    local configNode = root:AppendChild(LSXNode.new("region", { id = "Config" }))

    local rootNode = configNode:AppendChild(LSXNode.new("node", { id = "root" }))

    local rootChildren = rootNode:AppendChild(LSXHelpers.ChildrenNode())

    local conflictNode = rootChildren:AppendChild(LSXNode.new("node", { id = "Conflicts" }))

    local dependenciesNode = rootChildren:AppendChild(LSXNode.new("node", { id = "Dependencies" }))

    local moduleIndoNode = rootChildren:AppendChild(LSXNode.new("node", { id = "ModuleInfo" }))

    local moduleInfoAttr = {
        LSXHelpers.AttrNode("Author", "LSString", author),
        LSXHelpers.AttrNode("CharacterCreationLevelName", "FixedString", ""),
        LSXHelpers.AttrNode("Description", "LSString", description),
        LSXHelpers.AttrNode("Folder", "LSString", internalName),
        LSXHelpers.AttrNode("LobbyLevelName", "FixedString", ""),
        LSXHelpers.AttrNode("MD5", "LSString", ""),
        LSXHelpers.AttrNode("MainMenuBackgroundVideo", "FixedString", ""),
        LSXHelpers.AttrNode("MenuLevelName", "FixedString", ""),
        LSXHelpers.AttrNode("Name", "LSString", modName),
        LSXHelpers.AttrNode("NumPlayers", "uint8", "4"),
        LSXHelpers.AttrNode("PhotoBooth", "FixedString", ""),
        LSXHelpers.AttrNode("StartupLevelName", "FixedString", ""),
        LSXHelpers.AttrNode("Tags", "LSString", ""),
        LSXHelpers.AttrNode("Type", "FixedString", "Add-on"),
        LSXHelpers.AttrNode("UUID", "FixedString", uuid),
        LSXHelpers.AttrNode("Version64", "int64", version64),
    }

    moduleIndoNode:AppendChildren(moduleInfoAttr)

    local moduleInfoChildren = moduleIndoNode:AppendChild(LSXHelpers.ChildrenNode())

    local publishVersionNode = moduleInfoChildren:AppendChild(LSXNode.new("node", { id = "PublishVersion" }))

    local publishVersion64Attr = publishVersionNode:AppendChild(
        LSXHelpers.AttrNode("Version64", "int64", version64)
    )

    return root, conflictNode, dependenciesNode
end
