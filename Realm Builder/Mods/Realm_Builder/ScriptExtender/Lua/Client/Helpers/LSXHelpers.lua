    return childrenNode
end

---@param displayName string the handle of the translated string
---@param internalName string
---@param uiColor vec4
---@param matPUuid GUIDSTRING
---@param uuid GUIDSTRING
---@param presetType "CharacterCreationEyeColors"|"CharacterCreationHairColors"|"CharacterCreationSkinColors"
---@return LSXNode
function LSXHelpers.BuildCCAPresetNode(displayName, internalName, uiColor, matPUuid, uuid, presetType)
    if presetType:sub(-1) == "s" then
        presetType = presetType:sub(1, -2)
    end

    local node = LSXNode.new("node", { id = presetType })

    if #uiColor < 4 then
        uiColor = { uiColor[1] or 1, uiColor[2] or 1, uiColor[3] or 1, uiColor[4] or 1 }
    end

    local hex = RGBAToHex(uiColor[1], uiColor[2], uiColor[3], uiColor[4])

    local attrs = {
        LSXHelpers.AttrNode("DisplayName", "TranslatedString", displayName, 1),
        LSXHelpers.AttrNode("MaterialPresetUUID", "guid", matPUuid),
        LSXHelpers.AttrNode("Name", "FixedString", internalName),
        LSXHelpers.AttrNode("UUID", "guid", uuid),
        LSXHelpers.AttrNode("UIColor", "LSString", hex),
    }

    node:AppendChildren(attrs)

    return node
end

---@param presetsType "CharacterCreationEyeColors"|"CharacterCreationHairColors"|"CharacterCreationSkinColors"
