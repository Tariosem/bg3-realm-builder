local LSXNode = LSXNode
LSXHelpers = LSXHelpers or {}

--- Create a new LSX save root node
---@return LSXNode
function LSXHelpers.new()
    local save = {
        version = {
            major = 4,
            minor = 8,
            revision = 0,
            build = 400,
            lslib_meta = "v1,bswap_guids,lsf_keys_adjacency"
        },
    }

    local root = LSXNode.new("save")
    local versionNode = root:AppendChild(
        LSXNode.new("version", {
            major = save.version.major,
            minor = save.version.minor,
            revision = save.version.revision,
            build = save.version.build,
            lslib_meta = save.version.lslib_meta
        })
    )
    versionNode:SetAttrOrder({ "major", "minor", "revision", "build", "lslib_meta" })

    versionNode:AddComment("This file was generated by Script Extender")

    return root
end

local attrOrder = {
    "id",
    "type",
    "value",
    "handle",
    "version",
}
---@param id any
---@param attrType LSValueType
---@param value any
---@param version number? for translatedstring
---@return LSXNode
function LSXHelpers.AttrNode(id, attrType, value, version)
    local valueKey = attrType == "TranslatedString" and "handle" or "value"

    if attrType == "float" or attrType == "double" then
        value = tostring(value)
        if not value:find("%.") then
            value = value .. ".0"
        end
    end

    local attrNode = LSXNode.new("attribute", {
        id = id,
        type = attrType,
        [valueKey] = value,
    })

    if version and attrType == "TranslatedString" then
        attrNode:SetAttribute("version", version)
    end

    attrNode:SetAttrOrder(attrOrder)
    return attrNode
end

local lsattrNode = LSXHelpers.AttrNode

function LSXHelpers.GetPathAfterData(path)
    return path:match("Data[\\/](.*)") or path
end

--- @return LSXNode -- childrenNode
function LSXHelpers.BuildTemplatesRegionNode()
    return LSXHelpers.new():AppendChild(LSXNode.new("region", { id = "Templates" }))
        :AppendChild(LSXNode.new("node", { id = "Templates" }))
        :AppendChild(LSXHelpers.ChildrenNode())
end

function LSXHelpers.BuildLayerListNode(levelName)
    local layerNode = LSXNode.new("node", { id = "LayerList" })

    layerNode:AppendChild(LSXHelpers.ChildrenNode())
        :AppendChild(LSXNode.new("node", { id = "Layers" }))
        :AppendChild(LSXHelpers.ChildrenNode())
        :AppendChild(LSXNode.new("node", { id = "Object", key = "MapKey" })):SetAttrOrder({ "id", "key" })
        :AppendChild(lsattrNode("MapKey", "FixedString", levelName))

    return layerNode
end

--- @param node LSXNode
function LSXHelpers.SetAttrNode(node, attrId, attrType, value)
    local attrNode = node:SearchChild(function(child)
        if child:GetName() ~= "attribute" then return false end
        return child:GetAttribute("id") == attrId
    end)
    if not attrNode then
        attrNode = lsattrNode(attrId, attrType, value)
        node:AppendChild(attrNode)
    else
        attrNode:SetAttribute("type", attrType)
        attrNode:SetAttribute("value", value)
    end
end

--- @param data EntityData
--- @return LSXNode[]
local function buildItemAttrs(data)
    local attrs = {
        lsattrNode("CanBeMoved", "bool", data.Movable and true or false),
        lsattrNode("IsInteractionDisabled", "bool", data.CanInteract and false or true),
        lsattrNode("GravityType", "uint8", data.DisableGravityUntilMoved and 2 or data.Gravity and 0 or 1), -- 0 = Enabled, 1 = Disabled, 2 = Disabled until moved
        lsattrNode("CanBePickedUp", "bool", data.CanBeLooted and true or false),
    }

    return attrs
end

local function buildCharacterAttrs(entity)
    local attrs = {
        lsattrNode("CanFight", "bool", false), -- default to false for now
    }
    return attrs
end

--- @type table<string, fun(entity:EntityData):LSXNode[]>
local uinqueAttrs = {
    item = buildItemAttrs,
    character = buildCharacterAttrs,
}

--- @class WanderConfig
--- @field Area number?
--- @field WanderMin number
--- @field WanderMax number
--- @field SleepMin number?
--- @field SleepMax number?

--- build a local template
--- @param guid any
--- @param internalName string
--- @param displayNameHandle string?
--- @param entData EntityData
--- @return LSXNode?, {Uuid:string, TemplateType:string, LSXNode:LSXNode}[]?
function LSXHelpers.BuildTemplate(guid, entData, internalName, displayNameHandle)
    local curLevel = entData.LevelName
    local pos = entData.Position or { CGetPosition(guid) }
    local rot = entData.Rotation or { CGetRotation(guid) }
    local scale = entData.Scale or ({ CGetScale(guid) })[1]

    local templateType = entData.TemplateType
    local templateUuid = TakeTailTemplate(entData.TemplateId)
    local others = {}

    if not templateType then return end

    local templateRegion = LSXHelpers.BuildTemplatesRegionNode()
    local gameObjectNode = templateRegion:AppendChild(LSXNode.new("node", { id = "GameObjects" }))

    local basicAttrs = {
        lsattrNode("MapKey", "FixedString", guid),
        lsattrNode("Name", "LSString", internalName),
        lsattrNode("LevelName", "FixedString", curLevel),
        lsattrNode("Type", "FixedString", templateType),
        lsattrNode("TemplateName", "FixedString", templateUuid),
        lsattrNode("Flag", "uint8", entData.Flag or 1),
        lsattrNode("ParentTemplateId", "FixedString", templateUuid),
    }
    if templateType == "item" and entData.OverrideVisualUuid then
        gameObjectNode:InsertChild(LSXHelpers.AttrNode("VisualTemplate", "FixedString", entData.OverrideVisualUuid), 1)
    end

    if displayNameHandle then
        table.insert(basicAttrs, lsattrNode("DisplayName", "TranslatedString", displayNameHandle, 1))
    end

    if uinqueAttrs[templateType] then
        local customAttrs = uinqueAttrs[templateType](entData) --[[@as LSXNode[] ]]
        for _, attrNode in ipairs(customAttrs) do
            table.insert(basicAttrs, attrNode)
        end
    end

    gameObjectNode:AppendChildren(basicAttrs)

    local secondChidren = gameObjectNode:AppendChild(LSXHelpers.ChildrenNode())
    local transformNode = secondChidren:AppendChild(LSXNode.new("node", { id = "Transform" }))

    local transformAttrs = {
        lsattrNode("Position", "fvec3", pos),
        lsattrNode("RotationQuat", "fvec4", rot),
        lsattrNode("Scale", "float", scale),
    }

    transformNode:AppendChildren(transformAttrs)

    secondChidren:AppendChild(LSXHelpers.BuildLayerListNode(curLevel))

    if templateType == "character" then
        secondChidren:AppendChild(LSXNode.new("node", { id = "LocomotionParams" }))
        
        if entData.OverrideVisualUuid then
            gameObjectNode:InsertChild(LSXHelpers.AttrNode("CharacterVisualResourceID", "FixedString", entData.OverrideVisualUuid), 1)
        end

        if not entData.WanderConfig then
            return gameObjectNode, others
        end

        local wanderTriggerUuid = Uuid_v4()
        local wanderParams = entData.WanderConfig or {}
        local triggerName = internalName .. "_WanderTrigger"
        local anubisName = triggerName .. "_" .. wanderTriggerUuid

        local triggerNode = LSXHelpers.BuildBoxTrigger(pos, rot, wanderParams.Extents or {10,5,10},
            curLevel, triggerName, wanderTriggerUuid)

        local anubisAttr, anubisParams = AnubisHelpers.BuildWanderParams(
            entData.WanderConfig.WanderMin or 3,
            entData.WanderConfig.WanderMax or 6,
            entData.WanderConfig.SleepMin or 8,
            entData.WanderConfig.SleepMax or 14,
            anubisName)

        gameObjectNode:InsertChild(anubisAttr, 1)
        secondChidren:AppendChild(anubisParams)
        table.insert(others, { Uuid = wanderTriggerUuid, TemplateType = "trigger", LSXNode = triggerNode })
    end

    return gameObjectNode, others
end


function LSXHelpers.BuildRootTemplate(srcUuid, uuid, internalName, override)
    srcUuid = TakeTailTemplate(srcUuid)
    override = override or {}
    local templateRegion = LSXHelpers.BuildTemplatesRegionNode()
    local gameObjectNode = templateRegion:AppendChild(LSXNode.new("node", { id = "GameObjects" }))

    local parentType = Ext.Template.GetTemplate(srcUuid).TemplateType

    local basicAttrs = {
        lsattrNode("Type", "FixedString", parentType),
        lsattrNode("MapKey", "FixedString", uuid),
        lsattrNode("Name", "LSString", internalName),
        lsattrNode("ParentTemplateId", "FixedString", srcUuid),
    }

    if override.VisualTemplate then
        table.insert(basicAttrs, lsattrNode("VisualTemplate", "FixedString", override.VisualTemplate))
    end

    gameObjectNode:AppendChildren(basicAttrs)

    return gameObjectNode
end

function LSXHelpers.ChildrenNode()
    return LSXNode.new("children")
end

---@param names string[]
---@param version number|fun(name:string):number
---@return string, table<string, string[]>, table<string, string> -- content, string -> handle[], handle -> string
function LSXHelpers.GenerateLocalization(names, version)
    local root = LSXNode.new("contentList", {
        ["xmlns:xsd"] = "http://www.w3.org/2001/XMLSchema",
        ["xmlns:xsi"] = "http://www.w3.org/2001/XMLSchema-instance",
    })
    local stringToHandles = {}
    local handleToString = {}

    for _, name in ipairs(names) do
        local ver = 1
        if type(version) == "function" then
            ver = version(name)
        elseif type(version) == "number" then
            ver = version
        end

        local handle = MakeTranslatedHandle()

        local contentNode = LSXNode.new("content", {
            contentuid = handle,
            version = ver
        })
        contentNode:SetInnerText(name)
        root:AppendChild(contentNode)
        stringToHandles[name] = stringToHandles[name] or {}
        handleToString[handle] = name
        table.insert(stringToHandles[name], handle)
    end

    local content = root:Stringify({ Indent = 4, IncludeHeader = false })
    content = '<?xml version="1.0" ?>\n' .. content

    return content, stringToHandles, handleToString
end

--- @param handleToName table<TranslatedString, string>
--- @param versions table<TranslatedString, number>?
--- @return string
function LSXHelpers.BuildLocalization(handleToName, versions)
    local root = LSXNode.new("contentList", {
        ["xmlns:xsd"] = "http://www.w3.org/2001/XMLSchema",
        ["xmlns:xsi"] = "http://www.w3.org/2001/XMLSchema-instance",
    })

    for handle, name in pairs(handleToName) do
        local ver = 1
        if versions and versions[handle] then
            ver = versions[handle]
        end

        local contentNode = LSXNode.new("content", {
            contentuid = handle,
            version = ver
        })
        contentNode:SetInnerText(name)
        root:AppendChild(contentNode)
    end

    local content = root:Stringify({ Indent = 4, IncludeHeader = false })
    content = '<?xml version="1.0" ?>\n' .. content

    return content
end

function LSXHelpers.LSValueType(value)
    if type(value) == "boolean" then
        return LSValueType.bool
    elseif type(value) == "number" then
        return LSValueType.float
    elseif type(value) == "string" then
        return LSValueType.FixedString
    elseif type(value) == "table" then
        if #value == 1 then
            return LSValueType.float
        elseif #value == 2 then
            return LSValueType.fvec2
        elseif #value == 3 then
            return LSValueType.fvec3
        elseif #value == 4 then
            return LSValueType.fvec4
        end
    end

    return LSValueType.LSString
end

