--- generate a meta.lsx file for a mod
---@param uuid string
---@param modName string
---@param author string
---@param version vec4
---@param description string
---@return LSXNode, LSXNode, LSXNode -- root, conflictNode, dependenciesNode
function LSXHelpers.BuildModMeta(uuid, modName, internalName, author, version, description)
    uuid = uuid or Uuid_v4()
    modName = modName or "Unnamed Mod"
    author = author or "Unknown Author"
    version = version or { 1, 0, 0, 0 }
    description = description or ""

    local version64 = ComputeVersion64(version[1], version[2], version[3], version[4]) --[[@as number|string]]

    local root = LSXHelpers.new()
    root:GetChild(1):SetAttribute("lslib_meta", nil)

    local configNode = root:AppendChild(LSXNode.new("region", { id = "Config" }))

    local rootNode = configNode:AppendChild(LSXNode.new("node", { id = "root" }))

    local rootChildren = rootNode:AppendChild(LSXHelpers.ChildrenNode())

    local conflictNode = rootChildren:AppendChild(LSXNode.new("node", { id = "Conflicts" }))

    local dependenciesNode = rootChildren:AppendChild(LSXNode.new("node", { id = "Dependencies" }))

    local moduleIndoNode = rootChildren:AppendChild(LSXNode.new("node", { id = "ModuleInfo" }))

    local moduleInfoAttr = {
        lsattrNode("Author", "LSString", author),
        lsattrNode("CharacterCreationLevelName", "FixedString", ""),
        lsattrNode("Description", "LSString", description),
        lsattrNode("Folder", "LSString", internalName),
        lsattrNode("LobbyLevelName", "FixedString", ""),
        lsattrNode("MD5", "LSString", ""),
        lsattrNode("MainMenuBackgroundVideo", "FixedString", ""),
        lsattrNode("MenuLevelName", "FixedString", ""),
        lsattrNode("Name", "LSString", modName),
        lsattrNode("NumPlayers", "uint8", "4"),
        lsattrNode("PhotoBooth", "FixedString", ""),
        lsattrNode("StartupLevelName", "FixedString", ""),
        lsattrNode("Tags", "LSString", ""),
        lsattrNode("Type", "FixedString", "Add-on"),
        lsattrNode("UUID", "FixedString", uuid),
        lsattrNode("Version64", "int64", version64),
    }

    moduleIndoNode:AppendChildren(moduleInfoAttr)

    local moduleInfoChildren = moduleIndoNode:AppendChild(LSXHelpers.ChildrenNode())

    local publishVersionNode = moduleInfoChildren:AppendChild(LSXNode.new("node", { id = "PublishVersion" }))

    local publishVersion64Attr = publishVersionNode:AppendChild(
        lsattrNode("Version64", "int64", version64)
    )

    return root, conflictNode, dependenciesNode
end

---@param pos vec3
---@param extents vec3
---@param levelName string
---@param name string
---@return LSXNode
function LSXHelpers.BuildBoxTrigger(pos, rot, extents, levelName, name, uuid)
    local templateNode = LSXHelpers.BuildTemplatesRegionNode()
    local triggerNode = templateNode:AppendChild(LSXNode.new("node", { id = "GameObjects" }))

    local baseAttr = {
        lsattrNode("MapKey", "FixedString", uuid),
        lsattrNode("Extents", "fvec3", extents),
        lsattrNode("Flag", "int32", 1),
        lsattrNode("LevelName", "FixedString", levelName),
        lsattrNode("Name", "LSString", name),
        lsattrNode("OnlyCharacterEvents", "bool", false),
        lsattrNode("OnlyOsirisEvents", "bool", false),
        lsattrNode("TemplateName", "FixedString", "987b8a4d-2208-4bae-a92d-706db0e593e8"), -- BoxTrigger template
        lsattrNode("Type", "FixedString", "trigger"),
        lsattrNode("_OriginalFileVersion_", "int64", "144115207403209026"),
    }

    triggerNode:AppendChildren(baseAttr)
    local children = triggerNode:AppendChild(LSXHelpers.ChildrenNode())

    children:AppendChild(LSXNode.new("node", { id = "FadeChildren" }))
        :GetParent()
        :AppendChild(LSXHelpers.BuildLayerListNode(levelName))
        :GetParent()
        :AppendChild(LSXNode.new("node", { id = "Transform" }))
        :AppendChildren({
            lsattrNode("Position", "fvec3", pos),
            lsattrNode("RotationQuat", "fvec4", rot),
            lsattrNode("Scale", "float", 1),
        })

    return templateNode
end
